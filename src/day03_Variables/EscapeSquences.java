package day03_Variables;

/*
\n: new line (Enter)
\t:paraghraph space (tab)
\":double quote
\\:back slash


 */


public class EscapeSquences {

    public static void main(String[] args) {

        System.out.println("Hello everyone \nHow are you today ?");

        System.out.println("-----------------------------");
        System.out.println("Hello everyone\nI am UMUT\nI live in NYC");
        System.out.println("---------------------------------");
        System.out.println("\tToday is great day to study Java");

        System.out.println("-------------------------------------");
        System.out.println("My favorite TV series is \"GOT\".");
        System.out.println("-----------------------------------");
        System.out.println("/");
        System.out.println("\\");




    }






}
