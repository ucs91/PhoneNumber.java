package day03_Variables;

public class Print_VS_Print {

    public static void main(String[] args) {

        System.out.println("Hello Batch 23"); // appends new line after printing
        System.out.println("How are you today ?");

        System.out.println("--------------------------------------");
        System.out.print("Hello Batch 23"); // does not append new line
        System.out.print("How are you today?");

        System.out.println();

        System.out.println("--------------------------------------");
        System.out.println("JRE stands for Java Runtime Enviromemnt, JAVAC stands for JaVa Compiler,JDK stands for Java Development Kit,JVM stands for Java Virtual Machine   ");
        System.out.println();
        System.out.println("-----------------------------------------");
        System.out.print("JRE stands for Java Runtime Enviromemnt");
        System.out.print("JAVAC stands for JaVa Compiler");
        System.out.print("JDK stands for Java Development Kit");




    }




}
