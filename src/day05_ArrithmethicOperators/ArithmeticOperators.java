package day05_ArrithmethicOperators;

public class ArithmeticOperators {

    public static void main(String[] args) {
        int number = 100;

        boolean isEven = number%2 == 0; // if the remainder of number /2 is equal to zero, then number is even
        boolean isOdd  = number%2 > 0;

        System.out.println(100 +" is even number: " + isEven);
        System.out.println(100 +" is an Odd number: " + isOdd);






    }








}
