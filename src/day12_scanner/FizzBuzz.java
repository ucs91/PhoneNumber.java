package day12_scanner;

public class FizzBuzz {
    public static void main(String[] args) {

        int num=5;

        switch (num){

            case 3:
            case 6:
            case 9:
                System.out.println("fizz");
                break;
            case 5:
            case 10:
                System.out.println("buzz");
                break;
            default:
                System.out.println("invalid");








        }




    }










}
